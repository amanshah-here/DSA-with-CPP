#include<iostream>
using namespace std;

int main()
{
    int a = 10;
    int b = 43;

    cout << "sum:" << a + b << endl;

    cout << "difference:" << a - b << endl;

    cout << "product:" << a * b << endl;

    cout << "division:" << a / (double)b << endl;

    cout << "modulo:" << a % b << endl;
    return 0;
}